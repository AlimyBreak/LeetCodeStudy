

int majorityElement(int* nums, int numsSize)
{
    
    int i           = 0 ;
    int j           = 0 ;
    int ret_val     = 0 ;
    int vote        = 0 ;
    
    for(i = 0 ; i < numsSize  ; i++)
    {
        ret_val =   nums[i];
        vote    =   0;
        for(j=i;j<numsSize;j++)
        {
            if(nums[j]==ret_val)
            {
                vote++;
            }
            else
            {
                vote--;
            }
        }
        if(vote>=0)
        {
            break;
        }   
    }
    return ret_val;
}

