


int HammingDistance(int a , int b)
{
    int c       = a^b   ;
    int count   = 0     ;
    
    while(c!=0)
    {
        count ++;
        c = c & (c-1);
    }
    return count;
}

int totalHammingDistance(int* nums, int numsSize)
{
    int i = 0;
    int j = 0;
    int ret_val = 0;
    
    for(i = 0; i < numsSize ; i++)
    {
        for(j = i+1; j < numsSize ; j++)
        {
            ret_val += HammingDistance(nums[i],nums[j]);
        }
    }
    return ret_val;
}




